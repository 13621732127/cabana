export async function fetchRoutes(dongleId) {
    if(dongleId !== undefined) {
        dongleId = 'me';
    }
    const endpoint= ROUTES_ENDPOINT.replace('{dongleId}', dongleId);
    try {
        const resp = await fetch(endpoint);
        const routes = await resp.json();
        if('routes' in routes) {
            return routes;
        }
    } catch(err) {
        console.log(err);
    }
    return DEMO_ROUTES;
}

